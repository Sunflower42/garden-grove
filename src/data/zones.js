export function lookupZone(zipCode) {
  const prefix = String(zipCode).padStart(5, '0').slice(0, 3);
  const data = ZIP_ZONE_MAP[prefix];
  if (data) return { ...data, exact: true };

  // Fallback: try nearby prefixes
  const num = parseInt(prefix, 10);
  for (let offset = 1; offset <= 5; offset++) {
    const above = String(num + offset).padStart(3, '0');
    const below = String(num - offset).padStart(3, '0');
    if (ZIP_ZONE_MAP[above]) return { ...ZIP_ZONE_MAP[above], approximate: true };
    if (ZIP_ZONE_MAP[below]) return { ...ZIP_ZONE_MAP[below], approximate: true };
  }

  // Ultimate fallback — zone 6 (most common)
  return { zone: 6, lastFrost: '04-25', firstFrost: '10-10', fallback: true };
}

// Parse a "MM-DD" string into a local Date for the given year
// Using component constructor avoids UTC midnight timezone shift
export function parseLocalDate(mmdd, year = new Date().getFullYear()) {
  const [month, day] = mmdd.split('-').map(Number);
  return new Date(year, month - 1, day);
}

export function getFrostDates(zipCode, year = new Date().getFullYear()) {
  const data = lookupZone(zipCode);
  return {
    zone: data.zone,
    lastFrost: parseLocalDate(data.lastFrost, year),
    firstFrost: parseLocalDate(data.firstFrost, year),
  };
}
